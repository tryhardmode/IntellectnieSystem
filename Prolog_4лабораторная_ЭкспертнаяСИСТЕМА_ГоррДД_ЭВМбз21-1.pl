подходит(модель1, toyota, corolla).
подходит(модель2, hyundai, solaris).
подходит(модель3, bmw, x5).
подходит(модель4, honda, civic).
подходит(модель5, nissan, qashqai).

% Рекомендации по сезонности
рекомендация(лето, летние).
рекомендация(зима, зимние).
рекомендация(всесезонно, всесезонные).

% Рекомендации по условиям эксплуатации
условие(город, мягкая резина, хорошее сцепление на асфальте).
условие(трасса, износостойкость, устойчивость на высоких скоростях).
условие(бездорожье, глубокий протектор, повышенная проходимость).

% Правило подбора шин с учетом условий эксплуатации
подобрать_шину(Марка, Модель, Сезон, Ширина, Профиль, Диаметр, Условие, РекомендованнаяШина) :-
    шина(РекомендованнаяШина, Тип, Ширина, Профиль, Диаметр, Условие),
    подходит(РекомендованнаяШина, Марка, Модель),
    рекомендация(Сезон, Тип).

% Диалог с пользователем
начать :-
    write('Добро пожаловать в шинный центр!'), nl,
    write('Введите марку автомобиля: '), read(Марка),
    write('Введите модель автомобиля: '), read(Модель),
    write('Введите сезон (лето, зима, всесезонно): '), read(Сезон),
    write('Введите ширину шины: '), read(Ширина),
    write('Введите профиль шины: '), read(Профиль),
    write('Введите диаметр шины: '), read(Диаметр),
    write('Условия эксплуатации (город, трасса, бездорожье): '), read(Условие),
    ( 
        подобрать_шину(Марка, Модель, Сезон, Ширина, Профиль, Диаметр, Условие, Шина) -> 
        write('Рекомендуемая шина: '), write(Шина), nl 
        ;
        write('К сожалению, подходящих шин не найдено.'), nl
    ).
